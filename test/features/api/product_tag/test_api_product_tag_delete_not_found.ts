import api from "@ORGANIZATION/PROJECT-api";
import type { IAiCommerceAdmin } from "@ORGANIZATION/PROJECT-api/lib/structures/IAiCommerceAdmin";
import type { IAuthorizationToken } from "@ORGANIZATION/PROJECT-api/lib/structures/IAuthorizationToken";

/**
 * Validate error handling when attempting to delete a non-existent
 * product-tag binding as an admin.
 *
 * 1. Register a new admin account (join).
 * 2. Login with the admin credentials (if session reset needed).
 * 3. Attempt to delete a product-tag binding using a random (non-existent)
 *    UUID as productTagId.
 * 4. Check that the system returns an error (e.g., record not found).
 */
export async function test_api_product_tag_delete_not_found(
  connection: api.IConnection,
) {
  // 1. Register a new admin account (for authentication)
  const adminEmail: string = typia.random<string & tags.Format<"email">>();
  const adminPassword: string = RandomGenerator.alphabets(10);
  const adminJoin = await api.functional.auth.admin.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      status: "active",
    } satisfies IAiCommerceAdmin.IJoin,
  });
  typia.assert(adminJoin);

  // 2. (Optional) Login to reset session (should be authenticated by join already)
  await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });

  // 3. Attempt to delete a non-existent productTagId
  const invalidProductTagId: string & tags.Format<"uuid"> = typia.random<
    string & tags.Format<"uuid">
  >();
  await TestValidator.error(
    "should fail to delete non-existent product-tag binding",
    async () => {
      await api.functional.aiCommerce.admin.productTags.erase(connection, {
        productTagId: invalidProductTagId,
      });
    },
  );
}

