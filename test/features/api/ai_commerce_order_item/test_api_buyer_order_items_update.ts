/**
 * Validates buyer item search (ownership/filter logic, not mutation):
 *
 * 1. Register buyer_a, place order as buyer_a, query items as buyer_a (expect
 *    success)
 * 2. Register buyer_b, query buyer_a's order items as buyer_b (expect error)
 */
export async function test_api_buyer_order_items_update(
  connection: api.IConnection,
) {
  // Register buyer_a and authenticate
  const emailA = typia.random<string & tags.Format<"email">>();
  const passwordA = RandomGenerator.alphaNumeric(12);
  const buyerA = await api.functional.auth.buyer.join(connection, {
    body: { email: emailA, password: passwordA } satisfies IBuyer.ICreate,
  });
  typia.assert(buyerA);

  // Place order as buyer_a
  const orderInput = typia.random<IAiCommerceOrder.ICreate>();
  orderInput.buyer_id = buyerA.id;
  orderInput.channel_id = typia.random<string & tags.Format<"uuid">>();
  orderInput.address_snapshot_id = typia.random<string & tags.Format<"uuid">>();
  const order = await api.functional.aiCommerce.buyer.orders.create(
    connection,
    { body: orderInput },
  );
  typia.assert(order);

  // Query items as buyer_a (expect at least one item, all for this order)
  const page = await api.functional.aiCommerce.buyer.orders.items.index(
    connection,
    {
      orderId: order.id,
      body: {} satisfies IAiCommerceOrderItem.IRequest,
    },
  );
  typia.assert(page);
  TestValidator.equals(
    "all items' order_id match",
    page.data.every((i) => i.order_id === order.id),
    true,
  );
  TestValidator.predicate("buyer_a sees at least 1 item", page.data.length > 0);

  // Register buyer_b and authenticate
  const emailB = typia.random<string & tags.Format<"email">>();
  const passwordB = RandomGenerator.alphaNumeric(12);
  const buyerB = await api.functional.auth.buyer.join(connection, {
    body: { email: emailB, password: passwordB } satisfies IBuyer.ICreate,
  });
  typia.assert(buyerB);

  // Query buyer_a's order items as buyer_b (expect error)
  await TestValidator.error(
    "buyer_b forbidden from querying buyer_a's order items",
    async () => {
      await api.functional.aiCommerce.buyer.orders.items.index(connection, {
        orderId: order.id,
        body: {} satisfies IAiCommerceOrderItem.IRequest,
      });
    },
  );
}

