/**
 * Validates that an admin user can comment on an existing product inquiry
 * from a buyer.
 *
 * - Buyer is created and authenticated.
 * - Buyer creates a valid inquiry (against a random product).
 * - Admin user is created and authenticated (logged in).
 * - Admin posts a comment to the buyer's inquiry with required fields.
 * - The comment is checked for correct linkage and attribution.
 */
export async function test_api_admin_inquiry_comment_create_success(
  connection: api.IConnection,
) {
  // 1. Create buyer and login for inquiry creation
  const buyerEmail = typia.random<string & tags.Format<"email">>();
  const buyerPassword = RandomGenerator.alphaNumeric(10);
  const buyerJoin = await api.functional.auth.buyer.join(connection, {
    body: {
      email: buyerEmail,
      password: buyerPassword,
    } satisfies IBuyer.ICreate,
  });
  typia.assert(buyerJoin);

  // 2. Buyer creates a product inquiry
  // - Random valid product_id (simulate with random UUID)
  const productId = typia.random<string & tags.Format<"uuid">>();
  const inquiry = await api.functional.aiCommerce.buyer.inquiries.create(
    connection,
    {
      body: {
        product_id: productId,
        question: RandomGenerator.paragraph({ sentences: 5 }),
        visibility: "public",
      } satisfies IAiCommerceInquiry.ICreate,
    },
  );
  typia.assert(inquiry);

  // 3. Create admin and login
  const adminEmail = typia.random<string & tags.Format<"email">>();
  const adminPassword = RandomGenerator.alphaNumeric(12);
  const adminStatus = "active";
  await api.functional.auth.admin.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      status: adminStatus,
    } satisfies IAiCommerceAdmin.IJoin,
  });
  await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });

  // 4. Admin creates a comment on the inquiry
  const commentBody = RandomGenerator.paragraph({ sentences: 6 });
  const comment =
    await api.functional.aiCommerce.admin.inquiries.comments.create(
      connection,
      {
        inquiryId: inquiry.id,
        body: {
          body: commentBody,
        } satisfies IAiCommerceComment.ICreate,
      },
    );
  typia.assert(comment);

  // 5. Business and relational assertions
  TestValidator.equals(
    "comment linked to inquiry",
    comment.inquiry_id,
    inquiry.id,
  );
  TestValidator.equals("comment body matches", comment.body, commentBody);
  TestValidator.predicate(
    "author_id is UUID",
    typeof comment.author_id === "string" && comment.author_id.length === 36,
  );
  TestValidator.predicate(
    "comment created_at is valid date-time",
    typeof comment.created_at === "string" && comment.created_at.length > 0,
  );
  TestValidator.predicate(
    "comment status is string",
    typeof comment.status === "string",
  );
}

