/**
 * E2E test for permanent deletion of a store setting by admin.
 *
 * This test covers the lifecycle of store settings:
 *
 * 1. Admin joins and logs in.
 * 2. Seller joins and logs in.
 * 3. Seller creates a seller profile.
 * 4. Admin creates a store for that seller.
 * 5. Admin creates a store setting attached to the store.
 * 6. Admin deletes the store setting.
 * 7. Checks the store setting is removed (cannot be deleted again, and deletion
 *    with random id fails).
 * 8. Tests error cases for repeated/non-existent deletion.
 *
 * Audit log checks are omitted due to API access limitations.
 */
export async function test_api_store_setting_deletion_by_admin(
  connection: api.IConnection,
) {
  // 1. Admin registration and login
  const adminEmail = typia.random<string & tags.Format<"email">>();
  const adminPassword = RandomGenerator.alphaNumeric(12);
  const adminJoin = await api.functional.auth.admin.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      status: "active",
    } satisfies IAiCommerceAdmin.IJoin,
  });
  typia.assert(adminJoin);
  await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });

  // 2. Seller registration and login
  const sellerEmail = typia.random<string & tags.Format<"email">>();
  const sellerPassword = RandomGenerator.alphaNumeric(12);
  const sellerJoin = await api.functional.auth.seller.join(connection, {
    body: {
      email: sellerEmail,
      password: sellerPassword,
    } satisfies IAiCommerceSeller.IJoin,
  });
  typia.assert(sellerJoin);
  await api.functional.auth.seller.login(connection, {
    body: {
      email: sellerEmail,
      password: sellerPassword,
    } satisfies IAiCommerceSeller.ILogin,
  });

  // 3. Seller creates seller profile
  const sellerProfile =
    await api.functional.aiCommerce.seller.sellerProfiles.create(connection, {
      body: {
        user_id: sellerJoin.id,
        display_name: RandomGenerator.name(),
        approval_status: "active",
      } satisfies IAiCommerceSellerProfiles.ICreate,
    });
  typia.assert(sellerProfile);

  // 4. Switch to admin and create a store for this seller
  await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });
  const store = await api.functional.aiCommerce.admin.stores.create(
    connection,
    {
      body: {
        owner_user_id: sellerJoin.id,
        seller_profile_id: sellerProfile.id,
        store_name: RandomGenerator.paragraph({ sentences: 2 }),
        store_code: RandomGenerator.alphaNumeric(8),
        approval_status: "active",
      } satisfies IAiCommerceStores.ICreate,
    },
  );
  typia.assert(store);

  // 5. Admin creates a store setting
  const storeSetting =
    await api.functional.aiCommerce.admin.storeSettings.create(connection, {
      body: {
        store_id: store.id,
        settings_json: JSON.stringify({
          shipping: "express",
          return_days: 7,
        }),
        active: true,
      } satisfies IAiCommerceStoreSetting.ICreate,
    });
  typia.assert(storeSetting);

  // 6. Admin deletes the store setting
  await api.functional.aiCommerce.admin.storeSettings.erase(connection, {
    storeSettingId: storeSetting.id,
  });

  // 7. Attempt to delete again (should fail)
  await TestValidator.error(
    "Deleting an already deleted store setting must fail",
    async () => {
      await api.functional.aiCommerce.admin.storeSettings.erase(connection, {
        storeSettingId: storeSetting.id,
      });
    },
  );

  // 8. Attempt to delete a random, non-existent store setting (should fail)
  await TestValidator.error(
    "Deleting a non-existent store setting must fail",
    async () => {
      await api.functional.aiCommerce.admin.storeSettings.erase(connection, {
        storeSettingId: typia.random<string & tags.Format<"uuid">>(),
      });
    },
  );
}

