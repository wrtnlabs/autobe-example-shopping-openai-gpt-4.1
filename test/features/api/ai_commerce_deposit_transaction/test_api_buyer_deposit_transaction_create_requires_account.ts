import { ArrayUtil, RandomGenerator, TestValidator } from "@nestia/e2e";
import { IConnection } from "@nestia/fetcher";
import typia, { tags } from "typia";

import api from "@ORGANIZATION/PROJECT-api";
import type { IAiCommerceBuyer } from "@ORGANIZATION/PROJECT-api/lib/structures/IAiCommerceBuyer";
import type { IAiCommerceDepositTransaction } from "@ORGANIZATION/PROJECT-api/lib/structures/IAiCommerceDepositTransaction";
import type { IAuthorizationToken } from "@ORGANIZATION/PROJECT-api/lib/structures/IAuthorizationToken";
import type { IBuyer } from "@ORGANIZATION/PROJECT-api/lib/structures/IBuyer";

/**
 * Validate that a buyer cannot create a deposit transaction unless they have a
 * deposit account. Attempts to create a transaction without a deposit account
 * must fail with a business logic error indicating the missing linkage.
 *
 * Steps:
 *
 * 1. Register a new buyer (auth.buyer.join).
 * 2. Without creating any deposit account, attempt to create a deposit transaction
 *    for a random account.
 * 3. Confirm an error is thrown (business rule violation).
 */
export async function test_api_buyer_deposit_transaction_create_requires_account(
  connection: api.IConnection,
) {
  // 1. Register buyer (no deposit account)
  const buyerAuth: IAiCommerceBuyer.IAuthorized =
    await api.functional.auth.buyer.join(connection, {
      body: {
        email: typia.random<string & tags.Format<"email">>(),
        password: typia.random<
          string & tags.MinLength<8> & tags.MaxLength<128>
        >(),
      } satisfies IBuyer.ICreate,
    });
  typia.assert(buyerAuth);

  // 2. Attempt to create a deposit transaction (should fail)
  const transactionRequest = {
    deposit_account_id: typia.random<string & tags.Format<"uuid">>(), // Not actually linked
    type: RandomGenerator.pick([
      "recharge",
      "withdraw",
      "payment",
      "refund",
    ] as const),
    amount: 1000,
    status: "pending",
    performed_at: new Date().toISOString(),
  } satisfies IAiCommerceDepositTransaction.ICreate;

  await TestValidator.error(
    "buyer cannot create deposit transaction without a linked account",
    async () => {
      await api.functional.aiCommerce.buyer.depositTransactions.create(
        connection,
        { body: transactionRequest },
      );
    },
  );
}

