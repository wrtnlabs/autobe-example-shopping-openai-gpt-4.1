/**
 * Validates that an admin can soft-delete a specific comment on a buyer's
 * inquiry for moderation or compliance purposes. This covers the flow of
 * registering/joining and authenticating the admin and buyer, creating a
 * buyer inquiry, posting a comment, and then having the admin erase that
 * comment successfully using the admin endpoint. The test confirms proper
 * authentication flows and verifies the endpoint returns successfully.
 */
export async function test_api_admin_delete_inquiry_comment_success(
  connection: api.IConnection,
) {
  // 1. Register and authenticate admin
  const adminEmail = typia.random<string & tags.Format<"email">>();
  const adminPassword = RandomGenerator.alphaNumeric(12);
  const adminJoin = await api.functional.auth.admin.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      status: "active",
    } satisfies IAiCommerceAdmin.IJoin,
  });
  typia.assert(adminJoin);

  // 2. Register and authenticate buyer
  const buyerEmail = typia.random<string & tags.Format<"email">>();
  const buyerPassword = RandomGenerator.alphaNumeric(12);
  const buyerJoin = await api.functional.auth.buyer.join(connection, {
    body: {
      email: buyerEmail,
      password: buyerPassword,
    } satisfies IBuyer.ICreate,
  });
  typia.assert(buyerJoin);

  // 3. Buyer creates a new inquiry (simulate a product)
  // Assume product_id is a random UUID since no SDK/API to create a product is provided
  await api.functional.auth.buyer.login(connection, {
    body: {
      email: buyerEmail,
      password: buyerPassword,
    } satisfies IBuyer.ILogin,
  });

  const inquiry = await api.functional.aiCommerce.buyer.inquiries.create(
    connection,
    {
      body: {
        product_id: typia.random<string & tags.Format<"uuid">>(),
        question: RandomGenerator.paragraph(),
        visibility: "public",
      } satisfies IAiCommerceInquiry.ICreate,
    },
  );
  typia.assert(inquiry);

  // 4. Buyer posts a comment on the inquiry
  const comment =
    await api.functional.aiCommerce.buyer.inquiries.comments.create(
      connection,
      {
        inquiryId: inquiry.id,
        body: {
          body: RandomGenerator.paragraph(),
          status: "published",
        } satisfies IAiCommerceComment.ICreate,
      },
    );
  typia.assert(comment);

  // 5. Admin logs in for deletion
  await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });

  // 6. Admin deletes comment
  await api.functional.aiCommerce.admin.inquiries.comments.erase(connection, {
    inquiryId: inquiry.id,
    commentId: comment.id,
  });

  // No way to retrieve/delegate comment list in current SDK to verify comment absence.
  // If a listing endpoint existed, we would verify absence here.
  // However, absence of errors, and successful admin auth and erasure, validates the flow.
  TestValidator.predicate(
    "Admin successfully deleted comment (endpoint completed without error)",
    true,
  );
}

