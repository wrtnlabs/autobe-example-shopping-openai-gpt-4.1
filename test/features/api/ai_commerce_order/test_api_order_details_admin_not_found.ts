/**
 * Validate error handling when admin requests details for a non-existent
 * order.
 *
 * This test ensures that the API properly handles the case where an admin
 * user attempts to access order details using a random (fake) orderId that
 * does not exist in the system.
 *
 * Steps:
 *
 * 1. Register a new admin using unique email/password/status.
 * 2. Login as that admin to ensure proper authentication/context.
 * 3. Attempt to access order details with a randomly generated UUID orderId
 *    (guaranteed non-existent).
 * 4. Verify that the API responds with an error (such as 404 not found) and
 *    does not leak any sensitive data.
 *
 * The focus is on validating negative path and business error handling for
 * invalid resource lookup.
 */
export async function test_api_order_details_admin_not_found(
  connection: api.IConnection,
) {
  // 1. Register admin (join)
  const adminEmail = typia.random<string & tags.Format<"email">>();
  const adminPassword = RandomGenerator.alphaNumeric(12);
  const adminStatus = "active"; // assume 'active' is an allowed status
  const joinResult = await api.functional.auth.admin.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      status: adminStatus,
    } satisfies IAiCommerceAdmin.IJoin,
  });
  typia.assert(joinResult);

  // 2. Authenticate (login)
  const loginResult = await api.functional.auth.admin.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IAiCommerceAdmin.ILogin,
  });
  typia.assert(loginResult);

  // 3. Attempt GET order details with a random, non-existent orderId
  const fakeOrderId = typia.random<string & tags.Format<"uuid">>();
  await TestValidator.error(
    "admin cannot get details for non-existent orderId",
    async () => {
      await api.functional.aiCommerce.admin.orders.at(connection, {
        orderId: fakeOrderId,
      });
    },
  );
}

