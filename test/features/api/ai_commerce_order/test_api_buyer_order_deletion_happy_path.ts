/**
 * Happy path test: buyer can logically (soft) delete an own order that is
 * neither fulfilled, paid, nor closed.
 *
 * 1. Register a new buyer, authenticate (capture access token for context)
 * 2. Create a new order as this buyer (fresh, so status is eligible for
 *    deletion)
 * 3. Perform soft delete via DELETE /aiCommerce/buyer/orders/{orderId}
 * 4. Assert (if GET were available) that deleted_at is set (but since no GET
 *    endpoint present, rely on successful DELETE execution without error)
 * 5. (Negative not tested here: deletion of others' or already-fulfilled
 *    orders)
 */
export async function test_api_buyer_order_deletion_happy_path(
  connection: api.IConnection,
) {
  // 1. Register a new buyer and authenticate (session context set via token)
  const buyerInput = {
    email: typia.random<string & tags.Format<"email">>(),
    password: RandomGenerator.alphaNumeric(10),
  } satisfies IBuyer.ICreate;
  const buyerAuth = await api.functional.auth.buyer.join(connection, {
    body: buyerInput,
  });
  typia.assert(buyerAuth);

  // 2. Create a new eligible order as this buyer (simulate minimal valid payload)
  const orderInput = {
    buyer_id: buyerAuth.id,
    channel_id: typia.random<string & tags.Format<"uuid">>(),
    order_code: RandomGenerator.alphaNumeric(10),
    status: "created",
    total_price: 1000,
    currency: "USD",
    address_snapshot_id: typia.random<string & tags.Format<"uuid">>(),
    ai_commerce_order_items: [
      {
        product_variant_id: typia.random<string & tags.Format<"uuid">>(),
        item_code: RandomGenerator.alphaNumeric(8),
        name: RandomGenerator.paragraph({ sentences: 2 }),
        quantity: 1,
        unit_price: 1000,
        total_price: 1000,
      } satisfies IAiCommerceOrderItem.ICreate,
    ],
  } satisfies IAiCommerceOrder.ICreate;
  const order = await api.functional.aiCommerce.buyer.orders.create(
    connection,
    { body: orderInput },
  );
  typia.assert(order);

  // 3. Soft delete the order
  await api.functional.aiCommerce.buyer.orders.erase(connection, {
    orderId: order.id,
  });

  // 4. (If GET order endpoint existed, check that deleted_at is set.) Since only erase exists, test passes if no error thrown
}

